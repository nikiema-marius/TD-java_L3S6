public class Point{
    private int x;
    private int y;
    
    
    public Point(int x , int y){
        this.x = x;
        this.y = y;

    }

    public int setY(){
        this.y= y;
    }

    public int setX(){
        this.x= x;
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }
}